public class Aluno {
private Long id;
private String nome;
private String curso;

public Aluno(Long id, String nome, String curso){
    this.id=id;
    this.nome=nome;
    this.curso=curso;
}


public void Setid(){
    this.id=id;
}
public Long Getid(){
    return id;
}


public void setnome(){
    this.nome=nome;
}
public String Getnome(){
    return nome;
}

public void setcurso(){
    this.curso=curso;
}
public String getcurso(){
    return this.curso=curso;

}


}
